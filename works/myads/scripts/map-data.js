// Функция-генератор для детализации (запускается один раз при старте)
function getDetailedPoints() {
    let pts = [];
    WORLD_MAP_DATA.forEach(pt => {
        // Создаем "шум" вокруг каждой точки, чтобы заполнить пространство материка
        for(let i = 0; i < 6; i++) {
            pts.push({
                sP: Math.sin(pt.p + (Math.random()-0.5)*0.1),
                cP: Math.cos(pt.p + (Math.random()-0.5)*0.1),
                t: pt.t + (Math.random()-0.5)*0.15
            });
        }
    });
    return pts;
}